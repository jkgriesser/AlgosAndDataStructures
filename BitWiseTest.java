public class BitWiseTest {
    public static void main(String[] args) {
        System.out.println(1 << 3);
        System.out.println(-10 >> 1);
        System.out.println(-10 >>> 1);
        System.out.println(1 & 3);
        System.out.println(128 & 128);
        System.out.println((9 << 2));
        System.out.println((9 << 2) * 1.5);
    }
}
